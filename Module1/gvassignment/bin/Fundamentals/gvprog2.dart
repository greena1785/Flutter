void main() {
  int num1 = 20;
  int num2 = 16;

  //Addition
  print('Addition of $num1 And $num2 : ${num1 + num2}');

  //Subtraction
  print('Subtraction of $num1 And $num2 : ${num1 - num2}');

  //Multiplication
  print('Multiplication of $num1 And $num2 : ${num1 * num2}');

  //Division
  print('Division of $num1 And $num2 : ${num1 / num2}');

}